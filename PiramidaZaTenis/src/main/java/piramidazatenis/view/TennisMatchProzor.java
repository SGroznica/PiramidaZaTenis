    private void lstTennisMatchValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstTennisMatchValueChanged
        if (evt.getValueIsAdjusting() || lstTennisMatch.getSelectedValue() == null) {
            return;
        }
        obrada.setEntitet(lstTennisMatch.getSelectedValue());
        var e = obrada.getEntitet();
        txtRezultat.setText(e.getRezultat());
        txtTeren.setText(e.getTeren());
        //txtRed.setText(String.valueOf(e.getIgrac2().getRed()));
        if (e.getIgrac1() == null) {
            cmbNapada.setSelectedIndex(0);
        } else {
            cmbNapada.setSelectedItem(e.getIgrac1());
        }

        if (e.getIgrac2() == null) {
            cmbBrani.setSelectedIndex(0);
        } else {
            cmbBrani.setSelectedItem(e.getIgrac2());
        }

        if (e.getDatumigranja() != null) {
            dpDatum.setDate(e.getDatumigranja().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
        } else {
            dpDatum.setDate(null);
        }
        if (e.getPobjednik() == null) {
            cmbPobjednik.setSelectedIndex(0);
        } else {
            cmbPobjednik.setSelectedItem(e.getPobjednik());
        }

    }//GEN-LAST:event_lstTennisMatchValueChanged

    private void btnKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajActionPerformed

        Igrac napada = (Igrac) cmbNapada.getSelectedItem();
        Igrac brani = (Igrac) cmbBrani.getSelectedItem();
        Igrac pobjendik = (Igrac) cmbPobjednik.getSelectedItem();
        
        try {
           obrada.setEntitet(new TennisMatch()); 
           
             if (pobjendik == napada) {
                napada.setRed(brani.getRed());
                brani.setRed(brani.getRed()+1);

            }
            preuzmiVrijednosti();
            obrada.create();
            ucitaj();
            
          
        } catch (PiramidaZaTenisException ex) {
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }

    }//GEN-LAST:event_btnKreirajActionPerformed

    private void btnPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjeniActionPerformed
        if (obrada.getEntitet() == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite stavku");
            return;
        }
        preuzmiVrijednosti();

        try {
            obrada.update();
            ucitaj();
        } catch (PiramidaZaTenisException ex) {
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnPromjeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if (obrada.getEntitet() == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite stavku");
            return;
        }

        if (JOptionPane.showConfirmDialog(
                getRootPane(),
                "Sigurno obrisati \"" + obrada.getEntitet() + "\"?",
                "Brisanje",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION) {
            return;
        }

        try {
            obrada.delete();
            ucitaj();
        } catch (PiramidaZaTenisException ex) {
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void cmbNapadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbNapadaActionPerformed
        if (cmbNapada.getSelectedIndex() == 0) {
            ucitajIgrace1();
        }
        Igrac napadac = (Igrac) cmbNapada.getSelectedItem();
        DefaultComboBoxModel<Igrac> ig = new DefaultComboBoxModel<>();
        ObradaIgrac oi = new ObradaIgrac();
        List<Igrac> brani = oi.read();
        for (Igrac i : brani) {
            if (napadac.getRed() == i.getRed() + 1) {
                ig.addElement(i);
            }
        }
        cmbBrani.setModel(ig);

        ucitajPobjednika();
    }//GEN-LAST:event_cmbNapadaActionPerformed

